# Tuples 
# A built-in data type that lets us create immutable sequences of values.
# We can not cahange its values after the declaration

tup = (1, 2, 3, 4)
print(tup)
print(type(tup)) 
print(tup[0])
print(tup[1])

tup1 = ()  # This is an valid tuple
print(tup1)

tup2 = (1)  # this type of declaration is called as an integer declaration
print(type(tup2))
